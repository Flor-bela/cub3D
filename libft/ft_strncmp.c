#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t size)
{
	size_t			i;
	unsigned char	*s1;
	unsigned char	*s2;

	s1 = (unsigned char *)str1;
	s2 = (unsigned char *)str2;
	i = 0;
	while (i < size && s1[i] == s2[i] && s1[i] != '\0')
		i++;
	if (i == size)
		return (0);
	return (s1[i] - s2[i]);
}
