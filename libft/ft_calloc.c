#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*result;

	if ((count >= 65535 && size != 0) || (count != 0 && size >= 65535))
		return (NULL);
	result = malloc(count * size);
	if (result == NULL)
		return (NULL);
	ft_bzero(result, count * size);
	return (result);
}
