#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ch;
	size_t			i;

	ch = (unsigned char *)s;
	i = 0;
	while (n > 0)
	{
		ch[i] = '\0';
		i++;
		n--;
	}
}
